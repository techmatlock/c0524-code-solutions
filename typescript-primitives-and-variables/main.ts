const fullName: string = 'Mark Matlock';

console.log('value of fullName:', fullName);

const isCool: boolean = true;

console.log('value of isCool:', isCool);

const totalPets: number = 6;

console.log('value of totalPets:', totalPets);

// Typeof operator
console.log('typeof fullName:', typeof fullName);
console.log('typeof isCool:', typeof isCool);
console.log('typeof totalPets:', typeof totalPets);

// Undefined and null data types
const empty: null = null;
console.log('value of empty:', empty);
console.log('value of empty:', typeof empty);

const nothing: undefined = undefined;
console.log('value of nothing', nothing);
console.log('typeof nothing', typeof nothing);
